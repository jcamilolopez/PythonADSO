class Oscar:
    def __init__(self, index, year, age, name, movie):
        self.index = index
        self.year = year
        self.age = age 
        self.name = name
        self.movie = movie

    def getIndex(self):
        return self.index
    def getYear(self):
        return self. year
    def getAge(self):
        return self.age
    def getName(self):
        return self.name
    def getMovie(self):
        return self.movie
    
    